package com.comvee.cdms.checkresult.bo;

public class AddCheckoutDetailBO {

    private String code;
    private String value;
    private String name;
    private String unit;
    private String acronym;
    private String range;
    private String abnormalSign;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getAcronym() {
        return acronym;
    }

    public void setAcronym(String acronym) {
        this.acronym = acronym;
    }

    public String getRange() {
        return range;
    }

    public void setRange(String range) {
        this.range = range;
    }

    public String getAbnormalSign() {
        return abnormalSign;
    }

    public void setAbnormalSign(String abnormalSign) {
        this.abnormalSign = abnormalSign;
    }
}
